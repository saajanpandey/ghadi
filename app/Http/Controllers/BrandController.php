    }

    /**
     * Remove the specified resource from storage.
     *
    * @param  \App\Model\Brand  $brand
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $brand=Brand::find($id);
        $brand->delete();

        return redirect()->route('admin.index');

